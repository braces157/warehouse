    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if (option == 1) {
            if (!validateForm()) {
                return;
            }
            page.getCt().save(getForm());
            MessageDialog.info(this, "Thêm thành công");
            page.init();
            this.dispose();
        } else {
            if (!validateForm()) {
                return;
            }
            page2.getCtKH().save(getForm());
            MessageDialog.info(this, "Thêm thành công");
            page2.setKh(page2.getCtKH().findBySdt(txtSdt.getText()).get());
            page2.getTxtHoTenKH().setText(txtHoTen.getText());
            page2.getTxtSdtKH().setText(txtSdt.getText());
            this.dispose();
        }
    }//GEN-LAST:event_btnAddActionPerformed

