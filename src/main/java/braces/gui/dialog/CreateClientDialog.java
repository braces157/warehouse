    public KhachHang getForm() {
        KhachHang kh = new KhachHang();
        kh.setDiaChi(txtDiaChi.getText());
        kh.setSoDienThoai(txtSdt.getText());
        kh.setTenKhachHang(txtHoTen.getText());
        return kh;
    }

    public boolean validateForm() {
        if (Validation.isEmpty(txtHoTen.getText())) {
            MessageDialog.warring(this, "Tên không được để trống");
            return false;
        }
        if (Validation.isEmpty(txtSdt.getText())) {
            MessageDialog.warring(this, "Số điện thoại không được để trống");
            return false;
        }
        if (Validation.isEmpty(txtDiaChi.getText())) {
            MessageDialog.warring(this, "Địa chỉ không được để trống");
            return false;
        }
        if (!Validation.isPhoneNumber(txtSdt.getText())) {
            MessageDialog.warring(this, "Số điện thoại không hợp lệ");
            return false;
        }
        if (option == 1) {
            if (page.getCt().findBySdt(txtSdt.getText()).isPresent()) {
                MessageDialog.warring(this, "Số điện thoại đã tồn tại");
                return false;
            }
        } else {
            if (page2.getCtKH().findBySdt(txtSdt.getText()).isPresent()) {
                MessageDialog.warring(this, "Số điện thoại đã tồn tại");
                return false;
            }
        }
        return true;
    }

