    private void btnStatisticActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnStatisticActionPerformed
        list = tk.thongKeTheoThang(txtYear.getValue());
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        model.setRowCount(0);
        for (ThongKeDTO tkdto : list) {
            Object[] rowData = {tkdto.getThoiGian(), tkdto.getSoLuongNhap(), tkdto.getSoLuongXuat(),
                Formatter.formatVND(tkdto.getTongTienNhap()), Formatter.formatVND(tkdto.getTongTienXuat())};
            model.addRow(rowData);
        }
        loadChart();
    }//GEN-LAST:event_btnStatisticActionPerformed

