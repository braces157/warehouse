    void init() {
        JTableUtilities.setCellsAlignment(table, SwingConstants.CENTER);
        chart.addLegend("Chi ra", new Color(244, 67, 54));
        chart.addLegend("Thu vào", new Color(76, 175, 80));
        int currentYear = Year.now().getValue();
        DefaultTableModel model = (DefaultTableModel) table.getModel();
        TableRowSorter<DefaultTableModel> sorter = new TableRowSorter<>(model);
        sorter.setComparator(3, Comparator.comparingDouble(Formatter::unformatVND));
        sorter.setComparator(4, Comparator.comparingDouble(Formatter::unformatVND));
        table.setRowSorter(sorter);
        txtYear.setValue(currentYear);
    }

    void loadChart() {
        chart.clear();
        double sum_doanhthu = 0;
        double sum_chiphi = 0;
        for (int thang = 0; thang < list.size(); thang++) {
            sum_doanhthu = list.get(thang).getTongTienXuat();
            sum_chiphi = list.get(thang).getTongTienNhap();
            chart.addData(new ModelChart("Tháng " + (thang+1), new double[]{sum_chiphi, sum_doanhthu}));
        }
    }

